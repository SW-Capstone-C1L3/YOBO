package com.example.yobo_android.etc;

public class RecipeOrder {

    private String recipeOrderId;
    private String recipeOrderNumber;
    private int recipeOrderImageId;
    private String recipeOrderDescription;

    public String getRecipeOrderId() {
        return recipeOrderId;
    }

    public void setRecipeOrderId(String recipeOrderId) {
        this.recipeOrderId = recipeOrderId;
    }

    public String getRecipeOrderNumber() {
        return recipeOrderNumber;
    }

    public void setRecipeOrderNumber(String recipeOrderNumber) {
        this.recipeOrderNumber = recipeOrderNumber;
    }

    public int getRecipeOrderImageId() {
        return recipeOrderImageId;
    }

    public void setRecipeOrderImageId(int recipeOrderImageId) {
        this.recipeOrderImageId = recipeOrderImageId;
    }

    public String getRecipeOrderDescription() {
        return recipeOrderDescription;
    }

    public void setRecipeOrderDescription(String recipeOrderDescription) {
        this.recipeOrderDescription = recipeOrderDescription;
    }
}
